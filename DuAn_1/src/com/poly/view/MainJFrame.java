    public void setModelNV(NhanVien nv) {
        txtMaNhanVien.setText(nv.getMaNV().trim());
        txtTenNhanVien.setText(nv.getTenNV());
        txtMatKhau.setText(nv.getMatKhau());
        txtNgaySinh.setText(formater.format(nv.getNgaySinh()));
        boolean gt = nv.isGioiTinh();
        if (gt == true) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        cboVaiTro.setSelectedIndex(nv.getVaiTro());
        txtSodienthoai.setText(nv.getDienThoai());
        txtEmail.setText(nv.getEmail());
        if (nv.getHinh().equalsIgnoreCase("No avata")) {
            lblHinhAnh.setText("NO AVATA");
            lblHinhAnh.setIcon(null);
        } else {
            lblHinhAnh.setText("");
            ImageIcon img = new ImageIcon(getClass().getResource("/Image/" + nv.getHinh()));
            Image im = img.getImage();
            im.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getY(), 0);
            lblHinhAnh.setIcon(img);
        }
    }

    public void fillTableNV() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVien nv : nvDao.getAllNhanVien()) {
            Object rowData[] = new Object[9];
            rowData[0] = nv.getMaNV();
            rowData[1] = nv.getTenNV();
            rowData[2] = nv.getMatKhau();
            rowData[3] = formater.format(nv.getNgaySinh());
            rowData[4] = nv.isGioiTinh() ? "Nam" : "Nữ";
            rowData[5] = nv.getVaiTro();
            rowData[6] = nv.getDienThoai();
            rowData[7] = nv.getEmail();
            rowData[8] = nv.getHinh();
            model.addRow(rowData);
        }
    }

