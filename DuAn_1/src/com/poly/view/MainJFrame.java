    public void insertMatHang() {
        BangMatHang sanpham = new BangMatHang();
        SanPham matHang = (SanPham) cboMaMatHang.getSelectedItem();
        int SoLuong = (int) txtSoLuongHD.getValue();
        if (SoLuong <= 0) {
            JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0");
        } else {
            sanpham.setMatHang(matHang);
            sanpham.setSoLuong(SoLuong);
            bangmathang.add(sanpham);
            this.fillTableHDMH();
            double sum = 0;
            for (BangMatHang mathang : bangmathang) {
                sum += mathang.getSoLuong() * mathang.getMatHang().getDonGia();
            }
            txtTongTien.setText(String.valueOf(sum));
        }
    }

