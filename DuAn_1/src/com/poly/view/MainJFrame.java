    public void loadcbboxHDMH() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboMaMatHang.getModel();
        Connection con = null;
        PreparedStatement sttm = null;
        ResultSet rs = null;
        try {
            String sql1 = "select * from Hang_Hoa";
            con = JdbcHelper.getDBConnect();
            sttm = con.prepareStatement(sql1);
            rs = sttm.executeQuery();
            while (rs.next()) {
                SanPham sp = new SanPham();
                sp.setMaMatHang(rs.getString("MaMatHang"));
                sp.setMaLoaiHang(rs.getString("MaLoaiHang"));
                sp.setTenMatHang(rs.getString("TenMatHang"));
                sp.setSoLuong(rs.getInt("SoLuong"));
                sp.setDonGia(rs.getDouble("DonGia"));
                sp.setNgayNhap(rs.getDate("NgayNhap"));
                sp.setNgayHetHan(rs.getDate("NgayHetHang"));
                sp.setMaNhaCungCap(rs.getString("MaNhaCungCap"));
                sp.setTrangThai(rs.getString("TrangThai"));
                model.addElement(sp);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

