    private void btnXoaNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNVActionPerformed
        // TODO add your handling code here:
        if(!(ShareHelper.Manager() == 0)){
            JOptionPane.showMessageDialog(this, "Bạn không có quyền xóa nhân viên");
        }
        else{
            if (txtMaNhanVien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã NV","Lỗi",JOptionPane.ERROR_MESSAGE);
        } else {
            int ok = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (ok != JOptionPane.YES_NO_OPTION) {
                return;
            }
            if (nvDao.delete(txtMaNhanVien.getText()) > 0) {
                JOptionPane.showMessageDialog(this, "Delete thành công");
                fillTableNV();
                newsNV();
            }
        }
        }
        
    }//GEN-LAST:event_btnXoaNVActionPerformed

    private void btnThemNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNVActionPerformed
        // TODO add your handling code here:
        if(!(ShareHelper.Manager() == 0)){
            JOptionPane.showMessageDialog(this, "Bạn không có quyền thêm nhân viên");
        }
        else{
            if (CheckvalidateFormNV()) {
            try {
                NhanVien nv = getModelNV();
                if (txtXacNhan.getText().equals(txtMatKhau.getText())) {
                    if (nvDao.add(nv) > 0) {
                        JOptionPane.showMessageDialog(this, "Lưu Thành Công");
                        newsNV();
                        fillTableNV();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Không trùng mật khẩu","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            } catch (ParseException ex) {
                Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_btnThemNVActionPerformed

